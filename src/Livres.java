public class Livres {

    private String name;
    private String auteur;
    private String resume;
    private int colonne;
    private int rangee;
    private String parution;

    public Livres(String name, String auteur, String resume, int colonne, int rangee, String parution) {
        this.name = name;
        this.auteur = auteur;
        this.resume = resume;
        this.colonne = colonne;
        this.rangee = rangee;
        this.parution = parution;
    }
}
